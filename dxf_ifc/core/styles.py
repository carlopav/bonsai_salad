LINEWEIGHT_MAP: dict[int, float] = {
    -3: 0.25,
    -1: 0.25,
     0: 0.0,
    13: 0.13,
    18: 0.18,
    25: 0.25,
    35: 0.35,
    50: 0.50,
    70: 0.70,
   100: 1.00,
   140: 1.40,
   200: 2.00,
}

LINETYPE_PATTERNS: dict[str, list[tuple[float, float]]] = {
    "DASHED":      [(5.0, 2.5)],
    "HIDDEN":      [(3.0, 1.5)],
    "CENTER":      [(12.0, 3.0), (3.0, 3.0)],
    "PHANTOM":     [(12.0, 3.0), (3.0, 3.0), (3.0, 3.0)],
    "DOTTED":      [(0.0, 2.0)],
    "DASHDOT":     [(8.0, 3.0), (0.0, 3.0)],
    "CONTINUOUS":  [],
}


def aci_to_ifccolour(model: ifcopenshell.file, aci_index: int):
    """Return IfcColourRgb from an AutoCAD Color Index value."""
    rgb = ACI_TO_RGB.get(aci_index, (1.0, 1.0, 1.0))
    return model.createIfcColourRgb(None, *rgb)


def true_color_to_ifccolour(model: ifcopenshell.file, true_color: int):
    """Return IfcColourRgb from a 24-bit true color integer."""
    r = ((true_color >> 16) & 0xFF) / 255.0
    g = ((true_color >> 8) & 0xFF) / 255.0
    b = (true_color & 0xFF) / 255.0
    return model.createIfcColourRgb(None, r, g, b)


def lineweight_to_mm(lw_raw: int) -> float:
    """Convert DXF lineweight (hundredths of mm) to mm. -1 = BYLAYER, -3 = DEFAULT."""
    return LINEWEIGHT_MAP.get(lw_raw, 0.25)


def linetype_to_ifc_font(model: ifcopenshell.file, linetype_name: str):
    """Return IfcCurveStyleFont for known DXF linetype names, or None for CONTINUOUS."""
    name = linetype_name.upper() if linetype_name else "CONTINUOUS"
    pattern = LINETYPE_PATTERNS.get(name, [])
    if not pattern:
        return None
    segments = [
        model.createIfcCurveStyleFontPattern(
            VisibleSegmentLength=vis,
            InvisibleSegmentLength=inv,
        )
        for vis, inv in pattern
    ]
    return model.createIfcCurveStyleFont(Name=name, PatternList=segments)


def layer_colour(model: ifcopenshell.file, dxf_layer):
    """Resolve colour from a dxf layer object → IfcColourRgb."""
    try:
        tc = dxf_layer.dxf.true_color
        if tc is not None:
            return true_color_to_ifccolour(model, tc)
    except Exception:
        pass
    try:
        return aci_to_ifccolour(model, dxf_layer.color)
    except Exception:
        return model.createIfcColourRgb(None, 1.0, 1.0, 1.0)
